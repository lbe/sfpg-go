const getFileViewRowsByFolderID = `-- name: GetFileViewRowsByFolderID :many
SELECT id, folder_id, folder_path, path, filename, size_bytes, md5, mime_type
     , width, height, created_at, updated_at 
  FROM file_view
 WHERE folder_id = ?
 ORDER BY filename`

// GetFileViewRowsByFolderID retrieves all files in a folder by folder ID.
func (cq *CustomQueries) GetFileViewRowsByFolderID(ctx context.Context, folderID int64) (*sql.Rows, error) {
	return cq.query(ctx, cq.getFileViewRowsByFolderIDStmt, getFileViewRowsByFolderID, folderID)
}

const getFileViewRowsByFolderPath = `-- name: GetFileViewRowsByFolderPath :many
SELECT id, folder_id, folder_path, path, filename, size_bytes, md5, mime_type
     , width, height, created_at, updated_at 
  FROM file_view
 WHERE folder_path = ?
 ORDER BY filename`

// GetFileViewRowsByFolderPath retrieves all files in a folder by folder path.
func (cq *CustomQueries) GetFileViewRowsByFolderPath(ctx context.Context, folderPath string) (*sql.Rows, error) {
	return cq.query(ctx, cq.getFileViewRowsByFolderPathStmt, getFileViewRowsByFolderPath, folderPath)
}

const getFolderViewThumbnailBlobDataByPath = `-- name: GetFolderViewThumbnailBlobDataByPath :one 
SELECT tb.data
  FROM folder_paths fp
       INNER JOIN folders f
               ON fp.id = f.path_id
       INNER JOIN thumbs.thumbnail_blobs tb 
               ON f.tile_id = tb.thumbnail_id
 WHERE fp.path = ?`

// GetFolderViewThumbnailBlobDataByPath retrieves the thumbnail blob for a folder's
// tile image directly via the folder path. This query joins the main DB with the
// thumbnail blob store.
func (cq *CustomQueries) GetFolderViewThumbnailBlobDataByPath(ctx context.Context, path string) ([]byte, error) {
	row := cq.queryRow(ctx, cq.getFolderViewThumbnailBlobDataByPathStmt, getFolderViewThumbnailBlobDataByPath, path)
	var data []byte
	err := row.Scan(&data)
	return data, err
}

const getPreloadRoutesByFolderID = `-- name: GetPreloadRoutesByFolderID :many
WITH cte_cache_map AS (
    SELECT 'folder' AS cache_type, '/gallery/' AS cache_entry
    UNION ALL 
    SELECT 'folder' AS cache_type, '/info/folder/' AS cache_entry
    UNION ALL 
    SELECT 'file' AS cache_type, '/info/image/' AS cache_entry
    UNION ALL 
    SELECT 'file' AS cache_type, '/lightbox/' AS cache_entry
)
, cte_folders as (
    SELECT CASE WHEN f.parent_id IS NULL THEN 1 ELSE f.parent_id END AS parent_id, f.id AS folder_id, b.cache_entry || f.id as route 
      FROM folders f 
           INNER JOIN folders a 
                   ON f.parent_id = a.id 
           INNER JOIN cte_cache_map b 
                   ON b.cache_type = 'folder'
    UNION ALL 
    SELECT 1 AS parent_id, f.id AS folder_id, b.cache_entry || f.id as route 
      FROM folders f 
           CROSS JOIN cte_cache_map b 
                   ON b.cache_type = 'folder'
     WHERE f.parent_id is null 
)
, cte_files AS (
    SELECT CASE WHEN a.parent_id IS NULL THEN 1 ELSE a.parent_id END AS parent_id, f.folder_id, b.cache_entry || f.id as route
      FROM files f 
           INNER JOIN folders a 
                   ON f.folder_id = a.id 
           INNER JOIN cte_cache_map b 
                   ON b.cache_type = 'file'
)
, u AS (
    SELECT *
      FROM cte_folders
    UNION ALL
    SELECT *
      FROM cte_files
)
SELECT route 
  FROM u 
 WHERE ? IN (parent_id, folder_id)`

// GetPreloadRoutesByFolderID retrieves all preload routes for subfolders and files
// under the specified folder ID.
func (cq *CustomQueries) GetPreloadRoutesByFolderID(ctx context.Context, parent_id sql.NullInt64) (*sql.Rows, error) {
	return cq.query(ctx, cq.getPreloadRoutesByFolderIDStmt, getPreloadRoutesByFolderID, parent_id)
}

const getThumbnailBlobDataByID = `-- name: GetThumbnailBlobDataByID :one
SELECT data
  FROM thumbs.thumbnail_blobs 
 WHERE thumbnail_id = ?`

// GetThumbnailBlobDataByID retrieves thumbnail blob data by thumbnail ID.
// This method is now on CustomQueries to support the separate blob database.
func (cq *CustomQueries) GetThumbnailBlobDataByID(ctx context.Context, thumbnailID int64) ([]byte, error) {
	row := cq.queryRow(ctx, cq.getThumbnailBlobDataByIDStmt, getThumbnailBlobDataByID, thumbnailID)
	var data []byte
	err := row.Scan(&data)
	return data, err
}

const upsertThumbnailBlob = `-- name: UpsertThumbnailBlob :exec
INSERT INTO thumbs.thumbnail_blobs (thumbnail_id, data)
VALUES (?, ?)
    ON CONFLICT(thumbnail_id) 
    DO UPDATE SET data = excluded.data
            WHERE data IS NOT excluded.data`

// UpsertThumbnailBlobParams are the parameters for upserting a thumbnail blob.
type UpsertThumbnailBlobParams struct {
	ThumbnailID int64
	Data        []byte
}

// UpsertThumbnailBlob inserts or updates thumbnail blob data by thumbnail ID.
// This method is now on CustomQueries to support the separate blob database.
func (cq *CustomQueries) UpsertThumbnailBlob(ctx context.Context, arg UpsertThumbnailBlobParams) error {
	_, err := cq.exec(ctx, cq.upsertThumbnailBlobStmt, upsertThumbnailBlob, arg.ThumbnailID, arg.Data)
	return err
}

// WithTx returns a new CustomQueries instance with the provided transaction,
// allowing custom queries to be executed within a transaction. All prepared
// statements are propagated to the transactional instance.
func (cq *CustomQueries) WithTx(tx *sql.Tx) *CustomQueries {
	return &CustomQueries{
		Queries:                                  cq.Queries.WithTx(tx),
		getFileViewRowsByFolderIDStmt:            cq.getFileViewRowsByFolderIDStmt,
		getFileViewRowsByFolderPathStmt:          cq.getFileViewRowsByFolderPathStmt,
		getFolderViewThumbnailBlobDataByPathStmt: cq.getFolderViewThumbnailBlobDataByPathStmt,
		getPreloadRoutesByFolderIDStmt:           cq.getPreloadRoutesByFolderIDStmt,
		getThumbnailBlobDataByIDStmt:             cq.getThumbnailBlobDataByIDStmt,
		upsertThumbnailBlobStmt:                  cq.upsertThumbnailBlobStmt,
	}
}
